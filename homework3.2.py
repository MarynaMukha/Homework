# [12, 3, 4, 10] => [10, 12, 3, 4]
# [1] => [1]
# [] => []
# [12, 3, 4, 10, 8] => [8, 12, 3, 4, 10]

# создаем список
numbers: list[int] = [12, 3, 4, 10, 8]

# проверяем длину списка, если он больше 1го переносим последний елемент, если нет - выводим без изменений
if len(numbers) > 1:
    numbers.insert(0, numbers [-1])
    print("New list: ", numbers [:len(numbers)-1])
else:
    print(numbers[:])